CREATE OR REPLACE FUNCTION "public"."set_slug_from_name"() RETURNS "trigger"
    LANGUAGE "plpgsql"
    AS $$
begin
  new.slug := slugify(concat(new.name, '-', (new.id)::varchar));
  return new;
end
$$;

